#include "fillit.h"

int str_count(char *s)
{
	int i;
	int k;
	int j;

	i = 0;
	j = 0;
	k = 0;
	while (*s)
	{
		if (*s == '.')
			j++;
		if (*s == '#')
			i++;
		k++;
		s++;
	}
	return (j == 12 && i == 4 && k == 16 ? 1 : 0);
}

int str_position(char *s)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (*s)
	{
		i++;
		if (*s == '#' && j != 3)
		{
			if (*s == '#' && *(s + 1) == '#' && *(s + 4) == '#' && i < 13)
				j += 2;
			else if ((*(s + 1) == '#' && i % 4) || (*(s + 4) == '#' && i < 13)
				|| (i % 4 > 1 && *(s + 2) == '#' && i < 15 &&
				(i % 4 > 2 && *(s + 3) == '#' && i < 14) &&
				(i % 4 > 2 && *(s + 4) == '#' && i < 13)) ||
				(i % 4 > 2 && *(s + 3) == '#' && i < 14 &&
				(i % 4 > 2 && *(s + 4) == '#' && i < 13 )))
				j++;
		}
		s++;
	}
	return (j == 3 ? 1 : 0);
}
